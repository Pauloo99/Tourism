<h6><b>Day 2:</b> You will wake up early in the morning and head to the headquarters of the park to join other clients for briefing about dos and don’ts while with mountain gorillas on your habituation experience. After briefing, you will start your trek into the forest, guided by researchers and guides. You will have an opportunity to listen to and observe the researchers doing their work of habituating gorillas and if possible, you will be called upon to participate by collecting samples, naming of group members, making calls, and so much more.<br>
During your trek, you may encounter several birds, other animals, and primates in the forest. Spend extended time with mountain gorillas in their natural habitat in Bwindi Forest national park. It’s the best way to meet, see, interact face to face and enjoy the human like characters of mountain gorillas, which make them exceptional creatures.<br>
Much as most people mistake habituation the experience is a different story. It’s an experience of its own kind, which is surely worth doing by every gorilla lover. You may be wondering what the gorilla habituation experience is all about. The practice is all about training wild gorillas to make them familiar with human presence before they are declared free for trekking.
After a very long day, retire back to the camp for dinner and overnight. Having Enjoyed the best of Gorilla Habituation Safari in Uganda.

